/** 
 * Javascript Basics
 * Variables - Strings
 */

// Declare a variable
var word;

// Display value in the console
console.log(word); // undefined

// Set value
word = 'LFU';

// Display value in the console
console.log(word); // LFU

// Count the number of characters in the string
console.log(word.length); // 3

// Combine multiple strings
console.log('LFU' + ' ' + 'workshop'); // LFU workshop